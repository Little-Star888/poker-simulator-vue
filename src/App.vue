// Stores
const gameStore = useGameStore()

// Refs
const configPanelRef = ref<InstanceType<typeof ConfigPanel> | null>(null)

// UI State
const isDrawerOpen = ref(false)
const showScreenshotSelector = ref(false)
const showEndOfHandModal = ref(false)
const toastMessage = ref('')
const toastVisible = ref(false)
const toastType = ref('success') // 'success', 'error', 'info'
const isLoading = ref(false)

// Snapshot data
const snapshotPreviewImage = ref<string | null>(null)
const snapshotGameState = ref<any>(null)
const snapshotGtoSuggestions = ref<Record<string, any>>({})

// Methods
const toggleDrawer = () => {
  isDrawerOpen.value = !isDrawerOpen.value
}

const closeDrawer = () => {
  isDrawerOpen.value = false
}

// Screenshot handling
const handleScreenshotCapture = async (cropOptions: any) => {
  gameStore.log('üì∏ Ê≠£Âú®Ê†πÊçÆÈÄâÂÆöÂå∫ÂüüÁîüÊàêÂø´ÁÖß...')
  showScreenshotSelector.value = false

  try {
    const canvas = await html2canvas(document.body, {
      useCORS: true,
      backgroundColor: null,
      scale: 2,
      ...cropOptions
    })

    const imageData = canvas.toDataURL('image/png')
    gameStore.log('‚úÖ Êà™ÂõæÂ∑≤ÁîüÊàê„ÄÇÊ≠£Âú®Êï¥ÁêÜÂΩìÂâçGTOÂª∫ËÆÆ...')

    // Ëé∑ÂèñÊ∏∏ÊàèÁä∂ÊÄÅÂíå GTO Âª∫ËÆÆ
    const gameState = gameStore.game?.getGameState()

    // ‰∏éÂéüÁâàJS‰øùÊåÅ‰∏ÄËá¥ÔºöÂ∞ÜGTOÂª∫ËÆÆËΩ¨Êç¢‰∏∫ÂåÖÂê´phaseÂ≠óÊÆµÁöÑÂØπË±°ÁªìÊûÑ
    const allGtoSuggestions = gameStore.currentSuggestionsCache.map((item) => {
      return {
        playerId: item.playerId,
        suggestion: item.suggestion,
        request: item.request,
        phase: item.phase, // ‰øùÁïôÈò∂ÊÆµ‰ø°ÊÅØÔºå‰∏éÂéüÁâàJS‰øùÊåÅ‰∏ÄËá¥
        notes: "",
      };
    });

    gameStore.log('‚úÖ ÊâÄÊúâÂΩìÂâçGTOÂª∫ËÆÆÂ∑≤Êï¥ÁêÜ„ÄÇËØ∑Âú®ÂºπÁ™ó‰∏≠Á°ÆËÆ§‰øùÂ≠ò„ÄÇ')

    // ËÆæÁΩÆÂø´ÁÖßÊï∞ÊçÆ
    snapshotPreviewImage.value = imageData
    snapshotGameState.value = gameState
    snapshotGtoSuggestions.value = allGtoSuggestions // ‰ΩøÁî®ËΩ¨Êç¢ÂêéÁöÑÊï∞ÊçÆÁªìÊûÑ

    // ÊòæÁ§∫Âø´ÁÖßÁ°ÆËÆ§Ê®°ÊÄÅÊ°Ü
    gameStore.showSnapshotModal = true

  } catch (error: any) {
    gameStore.log('‚ùå Êà™ÂõæÂ§±Ë¥•: ' + error.message)
    console.error('Êà™ÂõæÂ§±Ë¥•:', error)
    snapshotPreviewImage.value = null
    snapshotGameState.value = null
    snapshotGtoSuggestions.value = {}
  }
}

const handleScreenshotCancel = () => {
  showScreenshotSelector.value = false
  gameStore.log('Êà™ÂõæÊìç‰ΩúÂ∑≤ÂèñÊ∂à„ÄÇ')

  // ÊâßË°åÂø´ÁÖßÂêéÁöÑÊìç‰ΩúÔºàÂ¶ÇÊûúÊúâÔºâ
  if (gameStore.postSnapshotAction) {
    gameStore.postSnapshotAction()
    gameStore.postSnapshotAction = null
  }
}

// Snapshot modal handling
const handleSnapshotConfirm = () => {
  // Áî± SnapshotModal ÂÜÖÈÉ®Â§ÑÁêÜ‰øùÂ≠òÈÄªËæë
  gameStore.log('Âø´ÁÖßÂ∑≤Á°ÆËÆ§‰øùÂ≠ò')
}

const handleSnapshotCancel = () => {
  gameStore.showSnapshotModal = false
  snapshotPreviewImage.value = null
  snapshotGameState.value = null
  snapshotGtoSuggestions.value = {}

  // ÊâßË°åÂø´ÁÖßÂêéÁöÑÊìç‰ΩúÔºàÂ¶ÇÊûúÊúâÔºâ
  if (gameStore.postSnapshotAction) {
    gameStore.postSnapshotAction()
    gameStore.postSnapshotAction = null
  }
}

const handleSnapshotRecapture = () => {
  gameStore.showSnapshotModal = false

  // Áü≠ÊöÇÂª∂ËøüÂêéÈáçÊñ∞ÊâìÂºÄÊà™ÂõæÈÄâÊã©Âô®
  setTimeout(() => {
    showScreenshotSelector.value = true
  }, 100)
}

const handleSnapshotSaved = (snapshotId: number) => {
  // Âà∑Êñ∞Âø´ÁÖßÂàóË°®
  if (configPanelRef.value) {
    configPanelRef.value.refreshSnapshotList()
  }

  // ÊâßË°åÂø´ÁÖßÂêéÁöÑÊìç‰ΩúÔºàÂ¶ÇÊûúÊúâÔºâ
  if (gameStore.postSnapshotAction) {
    gameStore.postSnapshotAction()
    gameStore.postSnapshotAction = null
  }

  // Ëá™Âä®ÊâìÂºÄÊü•ÁúãÂø´ÁÖßÊ®°ÊÄÅÊ°Ü
  setTimeout(() => {
    gameStore.currentViewSnapshotId = snapshotId
    gameStore.showViewSnapshotModal = true
  }, 300)
}

// View snapshot modal handling
const handleViewSnapshotClose = () => {
  gameStore.showViewSnapshotModal = false
  gameStore.currentViewSnapshotId = null
}

// End of hand modal handling
const handleEndOfHandConfirm = () => {
  console.log('[DEBUG] handleEndOfHandConfirm Ë¢´Ë∞ÉÁî®')
  console.log('[DEBUG] gameStore.snapshotDataForSave:', gameStore.snapshotDataForSave)
  showEndOfHandModal.value = false

  // ‰ΩøÁî®‰øùÂ≠òÁöÑÂø´ÁÖßÊï∞ÊçÆÔºåËÄå‰∏çÊòØcurrentSuggestionsCache
  if (gameStore.snapshotDataForSave) {
    console.log('[DEBUG] ËÆæÁΩÆÂø´ÁÖßÊï∞ÊçÆ - GTOÂª∫ËÆÆÊï∞Èáè:', gameStore.snapshotDataForSave.gtoSuggestions.length)
    console.log('[DEBUG] ËÆæÁΩÆÂø´ÁÖßÊï∞ÊçÆ - GTOÂª∫ËÆÆÂÜÖÂÆπ:', gameStore.snapshotDataForSave.gtoSuggestions)
    snapshotGtoSuggestions.value = gameStore.snapshotDataForSave.gtoSuggestions
    snapshotGameState.value = gameStore.snapshotDataForSave.gameState
    // ËÆæÁΩÆÂø´ÁÖßÂêéË¶ÅÊâßË°åÁöÑÊìç‰Ωú - Âú®Êà™ÂõæÂÆåÊàêÂêéÊâçÊ∏ÖÁ©∫ÁâåÊ°å
    gameStore.postSnapshotAction = () => {
      console.log('[DEBUG] ÊâßË°åpostSnapshotActionÔºåÁé∞Âú®Ê∏ÖÁ©∫ÁâåÊ°å')
      gameStore.stopGame()
      // Ê∏ÖÁêÜ‰øùÂ≠òÁöÑÊï∞ÊçÆ
      gameStore.snapshotDataForSave = null
    }
    // ÂêØÂä®Âø´ÁÖßÊµÅÁ®ã
    showScreenshotSelector.value = true
  } else {
    console.error('[ERROR] Ê≤°ÊúâÊâæÂà∞‰øùÂ≠òÁöÑÂø´ÁÖßÊï∞ÊçÆ')
    gameStore.stopGame()
  }
}

const handleEndOfHandCancel = () => {
  console.log('[DEBUG] handleEndOfHandCancel Ë¢´Ë∞ÉÁî®')
  showEndOfHandModal.value = false
  // Ê∏ÖÁêÜ‰øùÂ≠òÁöÑÊï∞ÊçÆ
  gameStore.snapshotDataForSave = null
  gameStore.stopGame()
}

// Toast notification
const showToast = (message: string, duration: number = 2000, type: string = 'success') => {
  toastMessage.value = message
  toastType.value = type
  toastVisible.value = true

  setTimeout(() => {
    toastVisible.value = false
    setTimeout(() => {
      toastMessage.value = ''
    }, 300)
  }, duration)
}

// Loader
const showLoader = () => {
  isLoading.value = true
}

const hideLoader = () => {
  isLoading.value = false
}

// Expose methods to global scope for use by other components
;(window as any).showScreenshotSelector = () => {
  if (!gameStore.isGameRunning) {
    gameStore.log('‚ö†Ô∏è Ê∏∏ÊàèÊú™ÂºÄÂßãÔºåÊó†Ê≥ï‰øùÂ≠òÂø´ÁÖß„ÄÇ')
    return
  }
  showScreenshotSelector.value = true
}

;(window as any).showEndOfHandModal = () => {
  console.log('[DEBUG] ÂÖ®Â±Ä showEndOfHandModal Ë¢´Ë∞ÉÁî®');
  showEndOfHandModal.value = true
}

;(window as any).showToast = showToast
;(window as any).showLoader = showLoader
;(window as any).hideLoader = hideLoader

// Initialize on mount
onMounted(() => {
  gameStore.log('Âæ∑Â∑ûÊâëÂÖã AI ÊµãËØïÊ®°ÊãüÂô®Â∑≤Âä†ËΩΩ')
